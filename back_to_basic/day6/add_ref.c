#include <stdio.h>

//인자로 정수형 포인터 변수를 받는다.
//즉 주소를 담고있는 놈을 매개변수로 받아야 한다.
void add(int *a){
    *a = (*a) + 10;
    //포인터 변수의 주소에 들어있는 값은 *a로 역참조가 가능한디
    // *a의 값에다가. *a의 값 + 10 을 한거다.
    // 즉 a의 주소에 담겨있는 값에 변형을 준거라 전달 가능.
}

int main(){
    int a = 7;
    add(&a);
    printf("%d\n", a);
    return 0;
}